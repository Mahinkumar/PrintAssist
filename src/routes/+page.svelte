<script>
  import { invoke } from "@tauri-apps/api/core"; 

  let name = $state("");
  let greetMsg = $state("");

  async function greet() {
    // Learn more about Tauri commands at https://tauri.app/develop/calling-rust/
    greetMsg = await invoke("greet", { name });
  }
</script>

